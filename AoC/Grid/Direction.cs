namespace AoC.Grid;

/// <summary>
/// Represents the possible directions for traversing a grid.
/// </summary>
public enum Direction
{
    /// <summary>
    /// The bottom (south) direction.
    /// </summary>
    Bottom = 0,

    /// <summary>
    /// The left (west) direction.
    /// </summary>
    Left,

    /// <summary>
    /// The right (east) direction.
    /// </summary>
    Right,

    /// <summary>
    /// The top (north) direction.
    /// </summary>
    Top
}
